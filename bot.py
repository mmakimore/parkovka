                logger.info(f"‚úÖ –ë—Ä–æ–Ω–∏—Ä–æ–≤–∞–Ω–∏–µ #{booking['booking_code']} –∑–∞–≤–µ—Ä—à–µ–Ω–æ (–≤—Ä–µ–º—è –∏—Å—Ç–µ–∫–ª–æ)")
                
